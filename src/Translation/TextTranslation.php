namespace Skyline\Translation\Translation;

/**
 * Proxy class, because you can use strings directly.
 * @package Skyline\Translation\Translation
 */
class TextTranslation implements TranslationInterface
{
    private $text;

    /**
     * TextTranslation constructor.
     * @param $text
     */
    public function __construct($text = "")
    {
        $this->text = $text;
    }


    /**
     * @return mixed
     */
    public function getText()
    {
        return $this->text;
    }

    /**
     * @param mixed $text
     */
    public function setText($text): void
    {
        $this->text = $text;
    }

    /**
     * @inheritDoc
     */
    public function __toString()
    {
        return $this->getText();
    }
}